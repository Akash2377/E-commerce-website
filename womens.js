function displayData(womensData) {
  womensData.map(function (el) {
    var input1 = `<img id="wphoto" src="${el.image_url}" alt="" />
        <h4 id="wname">${el.name}</h4>
        <div id="price">
          <h4 id="wprice">&#8377 ${el.price}</h4>
          <h4>&#8377 ${el.strikedoffprice}</h4>
        </div>
        <button id="addcart" onclick="addToCart(${el.id})">Add to cart</button>`;
    var div = document.createElement("div");
    div.innerHTML = input1;
    document.getElementById("womensPage").append(div);
  });
}
var myProduct = JSON.parse(localStorage.getItem("addToCart")) || [];

function addToCart(val) {
  var flag = true;
  for (var i = 0; i < myProduct.length; i++) {
    if (myProduct[i].id == val) {
      flag = false;
      break;
    }
  }
  if (flag) {
    for (var i = 0; i < womensData.length; i++) {
      if (womensData[i].id == val) {
        myProduct.push(womensData[i]);
      }
    }
    localStorage.setItem("addToCart", JSON.stringify(myProduct));
  }
}
